#!/usr/bin/env python3
"""Join run, claim, and blind-score artifacts into portable summaries."""
import argparse, csv, json
from collections import defaultdict
from pathlib import Path

def mean(values): return round(sum(values) / len(values), 2) if values else None

def main():
    parser = argparse.ArgumentParser(); parser.add_argument("--root", required=True, type=Path); args = parser.parse_args(); root = args.root.resolve()
    manifest = list(csv.DictReader((root / "run_manifest.csv").open(encoding="utf-8"))); claims = list(csv.DictReader((root / "claim_audit.csv").open(encoding="utf-8"))); scores = list(csv.DictReader((root / "blind_scores.csv").open(encoding="utf-8")))
    mapping = json.loads((root / "blind" / "mapping.json").read_text(encoding="utf-8")); sample_scores = {mapping[row["blind_id"]]: float(row["total_score"]) for row in scores}
    claim_status = defaultdict(lambda: defaultdict(int)); systems = defaultdict(list)
    for row in claims: claim_status[row["sample_id"]][row["verification_status"]] += 1
    for row in manifest: systems[row["system"]].append(row)
    table = []
    for system, rows in sorted(systems.items()):
        completed = [row for row in rows if row["status"] == "completed"]
        raw = [sample_scores[row["sample_id"]] for row in completed if row["group"] == "raw"]; controlled = [sample_scores[row["sample_id"]] for row in completed if row["group"] == "controlled"]
        supported = sum(claim_status[row["sample_id"]]["supported"] + claim_status[row["sample_id"]]["partially_supported"] for row in completed); total_claims = sum(sum(claim_status[row["sample_id"]].values()) for row in completed)
        table.append((system, len(rows), len(completed), mean([float(r["elapsed_seconds"]) for r in completed]), mean(raw), mean(controlled), supported, total_claims))
    lines = ["# Benchmark comparison\n\n> Generated by the public harness. Mock scores validate plumbing only; they are not research-quality evidence.\n\n| System | Planned | Completed | Mean seconds | Raw score | Controlled score | Supported claims | Audited claims |\n|---|---:|---:|---:|---:|---:|---:|---:|\n"]
    for row in table: lines.append("| {} | {} | {} | {} | {} | {} | {} | {} |\n".format(*["—" if value is None else value for value in row]))
    (root / "comparison_report.md").write_text("".join(lines), encoding="utf-8")
    total = len(manifest); completed = sum(row["status"] == "completed" for row in manifest)
    (root / "resume_metrics.md").write_text(f"# Reproducibility summary\n\n- Samples: {total}\n- Completed: {completed}\n- Failed: {total-completed}\n- Systems: {len(systems)}\n- Audited claims: {len(claims)}\n- Caveat: results are descriptive unless model, search backend, budget and judge are held constant.\n", encoding="utf-8")
    print(f"summarized {total} samples across {len(systems)} systems"); return 0

if __name__ == "__main__": raise SystemExit(main())
